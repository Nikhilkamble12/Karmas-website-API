import { DataTypes } from "sequelize";

const StatusMasterModel = (sequelize)=>{
    return sequelize.define("status_master",{
        status_id:{
            type: DataTypes.INTEGER,
            primaryKey: true,
            autoIncrement: true
        }, parent_id:{
            type:DataTypes.INTEGER,
            allowNull:true
        }, status_name:{
            type:DataTypes.STRING(255),
            allowNull:true
        }, is_active: {
            type: DataTypes.BOOLEAN,
            defaultValue: true
        },
        created_by: {
            type: DataTypes.INTEGER,
            allowNull: true
        },
        created_at: {
            type: DataTypes.DATE,
            allowNull: true,
            defaultValue: DataTypes.NOW
        },
        modified_by: {
            type: DataTypes.INTEGER,
            allowNull: true
        },
        modified_at: {
            type: DataTypes.DATE,
            allowNull: true
        },
        deleted_by: {
            type: DataTypes.INTEGER,
            allowNull: true
        },
        deleted_at: {
            type: DataTypes.DATE,
            allowNull: true
        }
    },{
        tableName: "status_master",
        paranoid: true,
        timestamps: false,
        defaultScope: {
            attributes: {
                exclude: [
                    "created_by",
                    "modified_by",
                    "deleted_by",
                    "deleted_at",
                    "is_active",
                    "created_at",
                    "modified_at",
                ],
            },
        }, 
    })
}
export default StatusMasterModel